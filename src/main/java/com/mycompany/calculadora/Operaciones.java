package com.mycompany.calculadora;

public class Operaciones {
    
    public double  result;
    
    public void suma(double elemento_1, double elemento_2){
        result = elemento_1 + elemento_2;
    }
    
    public void resta(double elemento_1, double elemento_2){
        result = elemento_1 - elemento_2;
    }
    
    public void multiplicacion(double elemento_1, double elemento_2){
        result = elemento_1 * elemento_2;
    }
    
    public void division(double elemento_1, double elemento_2){
        result = elemento_1 / elemento_2;
    }
    
}

